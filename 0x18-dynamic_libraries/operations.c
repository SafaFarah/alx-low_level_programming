#include <stdio.h>
/**
 * add - adds two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of adding two number
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substracts  two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of substraction two number.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiples two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of multiplying two number
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of division two number
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns reminder of  two numbers
 * @a: first number
 * @b: second number
 *
 * Return: returns reminder of  two numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
