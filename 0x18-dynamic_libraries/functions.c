#include <stdio.h>

/**
 * add - function adds two numbers.
 * @a: first number
 * @b: second number
 *
 * Return: result of adding two number
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function substracts  two numbers.
 * @a: first number
 * @b: second number
 *
 * Return: result of substraction two number
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function multiples two numbers.
 * @a: first number
 * @b: second number
 *
 * Return: result of Multiplying two number
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div  - function divides two numbers.
 * @a: first number
 * @b: second number
 *
 * Return: result of Division two number
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function returns Remainder of  two numbers.
 * @a: first number
 * @b: second number
 *
 * Return: Remainder of two number
 */
int mod(int a, int b)
{
	return (a % b);
}
